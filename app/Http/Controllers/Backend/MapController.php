        $buildings = Building::all();
        $x = 0;
        $mapcenter = Configurations::where('name', 'map-center-lat-long')->first();
        foreach ($buildings as $building){
            $buildings[$x]['i']=$x;
            $buildings[$x]['mapcenter']=$mapcenter['value'];
            $x++;
        }
        return view('backend.map.index', ['buildings' => $buildings]);
    }

