    public function update_role(Request $request)
    {
        $user = User::find($request->id);
        $role = Role::where('slug', $request->role)->first();
        $user->roles()->sync($role);

        if ($user->save()) {
            return response()->json(true);
        }
        else {
            return response()->json(false);
        }
    }
